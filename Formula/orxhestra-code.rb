  sha256 "42110ad0c524772ac080892a82e541a094021ac03e58bd096c22a2bc0e602cc7"
  license "Apache-2.0"

  depends_on "python@3.13"

  def install
    virtualenv_install_with_resources
  end

  test do
    assert_match "orx-coder", shell_output("#{bin}/orx-coder --help")
  end
end
